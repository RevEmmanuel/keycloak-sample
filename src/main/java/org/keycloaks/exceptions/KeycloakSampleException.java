package org.keycloaks.exceptions;

public class KeycloakSampleException extends Exception {

    public KeycloakSampleException(String message) {
        super(message);
    }

    public KeycloakSampleException(String message, Throwable cause) {
        super(message, cause);
    }

    public KeycloakSampleException(Throwable cause) {
        super(cause);
    }


}
